package com.zy.bean;

public class Unit {
 private int id;
 private String name;
 
public Unit() {
	super();
}
public Unit(int id, String name) {
	super();
	this.id = id;
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
 
}
