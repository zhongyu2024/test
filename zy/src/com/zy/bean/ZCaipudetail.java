package com.zy.bean;

public class ZCaipudetail {
private int id;
private String j;
private String n;
private float num;
private int materialsid;
private String es;
private String w;
public ZCaipudetail() {
	super();
}
public ZCaipudetail(int id, String j, String n, float num, int materialsid,
		String es, String w) {
	super();
	this.id = id;
	this.j = j;
	this.n = n;
	this.num = num;
	this.materialsid = materialsid;
	this.es = es;
	this.w = w;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getJ() {
	return j;
}
public void setJ(String j) {
	this.j = j;
}
public String getN() {
	return n;
}
public void setN(String n) {
	this.n = n;
}
public float getNum() {
	return num;
}
public void setNum(float num) {
	this.num = num;
}
public int getMaterialsid() {
	return materialsid;
}
public void setMaterialsid(int materialsid) {
	this.materialsid = materialsid;
}
public String getEs() {
	return es;
}
public void setEs(String es) {
	this.es = es;
}
public String getW() {
	return w;
}
public void setW(String w) {
	this.w = w;
}
 
  
}
