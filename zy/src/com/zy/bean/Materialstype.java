package com.zy.bean;

public class Materialstype {
 private int id;
 private String name;
 
 
public Materialstype() {
	super();
}
public Materialstype(int id, String name) {
	super();
	this.id = id;
	this.name = name;
}
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
 
}
